
# Ask the user if they have played before
show_instructions = input("Have you played this game before?".lower())

# If they say yes, output 'program continues'
if show_instructions == "yes":
    print("program continues")

# If they say no, output 'program continues'
elif show_instructions == "no":
    print("program continues")

# If they say y, output 'program continues'
elif show_instructions == "y":
    print("program continues")

# If they say n, output 'program continues'
elif show_instructions == "n":
    print("program continues")

# If they say something else, output 'please either "yes" or "no"'
else:
    print('Please either "yes" or "no"')
